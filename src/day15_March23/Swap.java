package day15_March23;

public class  Swap {

	public static void main(String[] args) {
		
//		 create two integer variables assign value
//		 swap thevalue of these two variable
//		 and print the out.
//		 using temp variable is allowed
		
		int num1 =10;
		int num2 = 20;
		int temp;
		
		
		temp = num1; //10
		
		num1 = num2; //20
		
		num2 = temp ;  //10
		
		System.out.println(num1  + ", "+num2);
		
	}

}
