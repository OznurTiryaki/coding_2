package day20_March31;

public class LoopPracticeDOWHILE {

	public static void main(String[] args) {
		
       
		
	//print our name char by using do while loop
		// oznur O z n u r
		
		String name= "oznur";
		
		int x =0;
		do {
			
		System.out.print(name.charAt(x)+ " ");
		x++;
		
		}while (x< name.length());
		
		System.out.println(); 
		System.out.println("Meaningful Name");
		
	}

}
