package day23_April6;

public class NestedLoop {

	public static void main(String[] args) {
	//print from 1-5

		
		
			
			for (int x =0 ; x<3 ;x ++) {	//diger loop'u bu loop kadar yazdir
				
				for (int y =1 ; y <6 ; y++) {
					System.out.print(y +" ");
		}
			
			System.out.println();
		}
	}

}
